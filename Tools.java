package pkg2;

public class Tools{
	public void Numbers(int n1) {
		for(int i=0;i<=n1;i++) {
			System.out.print(1*i+" ");
			}
			System.out.println("");
	}
		
		 
			
	public void Odd(int n1) {
		
		for(int i=0;i<=n1;i++) {
			System.out.print(2*i+1+" ");
		}
		System.out.println("");
	}
	
	public void Even(int n1) {
		for(int i=0;i<=n1;i++) {
			System.out.print(2*i+" ");
			 
		}
		System.out.println("");
	}
	
	
	public void Mult20(int n1) {
		for( int j=3;j<21;j++) {
			for(int i=0;i<=n1;i++) {
				System.out.print(j*i+" ");
		}
			System.out.println("");
		 
	}
	}
}
	
	
	
	
	 